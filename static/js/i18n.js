function t(key, params) {
  let s = I18N[currentLang]?.[key] || I18N.en?.[key] || key;
  if (params) {
    Object.entries(params).forEach(([k, v]) => { s = s.replace('{' + k + '}', v); });
  }
  return s;
}

const LANG_CYCLE = SUPPORTED_LANGS;

function cycleLang(){
  const idx=LANG_CYCLE.indexOf(currentLang);
  const next=LANG_CYCLE[(idx+1)%LANG_CYCLE.length];
  setLang(next);
}

function setLang(lang, saveToProfile = true) {
  if (!SUPPORTED_LANGS.includes(lang)) lang = 'en';
  currentLang = lang;
  localStorage.setItem('chef_lang', lang);
  document.documentElement.lang = lang;
  applyI18n();
  const btn = document.getElementById('langToggle');
  if (btn) btn.textContent = lang.toUpperCase();
  const ddLang=document.getElementById('ddLang');
  if(ddLang) ddLang.textContent='🌐 Język ('+lang.toUpperCase()+')';

  // Save to Supabase profile if logged in and user explicitly changed language
  if(saveToProfile && typeof authToken!=='undefined' && authToken && typeof API!=='undefined'){
    fetch(API+'/api/profile',{method:'POST',headers:{'Authorization':'Bearer '+authToken,'Content-Type':'application/json'},body:JSON.stringify({lang})}).catch(()=>{});
  }
}

function applyI18n() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    el.textContent = t(el.dataset.i18n);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    el.title = t(el.dataset.i18nTitle);
  });
}
